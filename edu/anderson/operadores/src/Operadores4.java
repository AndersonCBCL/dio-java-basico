public class Operadores4 {
    public static void main(String[] args) throws Exception {

        int numero = 5;

        // numero = numero + 1;
        numero++;

        System.out.println(numero ++);
        System.out.println(numero);
        System.out.println(++ numero);

        System.out.println(numero --);
        System.out.println(numero);

        
    }
}
